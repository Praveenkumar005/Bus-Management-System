    private void mobilenoTFKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mobilenoTFKeyPressed
        // TODO add your handling code here:
        String mobileno=mobilenoTF.getText();
        int length = mobileno.length();
        char c=evt.getKeyChar();
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9')
        {
            if(length<10)
            {
                mobilenoTF.setEditable(true);
            }else{
                 mobilenoTF.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                 mobilenoTF.setEditable(true);
            }else{
                 mobilenoTF.setEditable(false);
            }
        }
    }//GEN-LAST:event_mobilenoTFKeyPressed

