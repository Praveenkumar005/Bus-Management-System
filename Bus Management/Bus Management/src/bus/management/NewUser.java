    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        String firstname=firstnameTF.getText();
        String lastname=lastnameTF.getText();
        String username=usernameTF.getText();
        String password=passwordPF.getText();
        String email=emailTF.getText();
        String mobileno=mobilenoTF.getText();
        String sq= (String)jComboBox1.getSelectedItem();
        String answer= answerTF.getText();
        if(password.matches(".*[0-9]{1,}.*") && password.matches(".*[@#$]{1,},*") && password.matches(".*[A-Z]{1,},*") &&password.matches(".*[a-z]{1,},*") && password.length()>=8 && password.length()<=15)
        {
       
        
        try
        {    
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=  DriverManager.getConnection("jdbc:mysql://localhost:3306/busm","root","");
           Statement stat=con.createStatement();
           String selectQuery="select count(*) from user_details where username='"+username+"' and password='"+ password+"'";
           System.out.println(selectQuery);
           ResultSet rs=stat.executeQuery(selectQuery);
           System.out.println(rs.next());
           if(rs.next())
           {
               infoMessage("Already Registered","Welcome !!");
           }
           else
           {
            String insertQuery="insert into user_details values(null,'"+firstname+"','"+lastname+"','"+username+"','"+password+"','"+email+"','"+mobileno+"','"+sq+"','"+answer+"')";
          
            stat.executeUpdate(insertQuery);
          
            infoMessage("Information is Inserted","Alert !!");
            dispose();
            UserLogin ln=new UserLogin();
            ln.setLocationRelativeTo(null);
            ln.setVisible(true);
                
           }
           
       
        }
        catch(Exception e){
            System.out.println(e);
        
        }
        
        
        } 
        else{
            JOptionPane.showMessageDialog(null,"Must contain atleast one digit ,Must contain atleast one of the following special characters @#$");
        }
       
    }//GEN-LAST:event_registerBtnActionPerformed

